    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        boolean validador = ValidadorCampos.validarCampos(txtTitulo, txtAnio, txtFecha);
        if (validador) {
            Eleccion eleccion = new Eleccion();
            eleccion.setTitulo(txtTitulo.getText());
            eleccion.setAnio(Integer.parseInt(txtAnio.getText()));
            eleccion.setFechaElecciones(txtFecha.getText());
            
            control.registrar(eleccion);

            javax.swing.JOptionPane.showMessageDialog(this, "Registro exitoso.", "Registro", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe completar todos los campos.", "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

